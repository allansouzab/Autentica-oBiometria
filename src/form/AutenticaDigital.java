    public void rodarComparacao(String img_cadastrada, String img_escolhida) throws IOException {
        
	String img_diferenca = "diferenca.jpg";
	
	ImageComparison imageComparison = new ImageComparison(10,10,0.05);
	
	if(imageComparison.fuzzyEqual(img_cadastrada,img_escolhida,img_diferenca)){
            lblResultado.setForeground(Color.GREEN);
            lblResultado.setText("POSITIVO");
            btnEntrar.setEnabled(true);
        }     
        else{
            lblResultado.setForeground(Color.RED);
            lblResultado.setText("FALSO");
            btnEntrar.setEnabled(false);
        }
    }
    
