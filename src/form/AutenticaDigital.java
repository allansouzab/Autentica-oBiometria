    private void btnColocarDigitalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnColocarDigitalActionPerformed
        // TODO add your handling code here:
        JFileChooser chooser = new JFileChooser();
        FileNameExtensionFilter filter = new FileNameExtensionFilter("JPG Images", "jpg");
        chooser.setFileFilter(filter);
        int returnVal = chooser.showOpenDialog(getParent());
        if(returnVal == JFileChooser.APPROVE_OPTION){
            
            String path_cadastrada = digital;
            String path_escolhida = chooser.getSelectedFile().getPath();
            String path_diferenca = "C:\\Users\\allan\\Documents\\NetBeansProjects\\aut com biometria\\diferenca.jpg";
            
            ImageIcon img = new ImageIcon(chooser.getSelectedFile().getPath());
            Image image = img.getImage();
            Image newimg = image.getScaledInstance(lblDigitalEscolhida.getWidth(), lblDigitalEscolhida.getHeight(), java.awt.Image.SCALE_SMOOTH);
            lblDigitalEscolhida.setIcon(new ImageIcon(newimg));
            
            try{
                rodarComparacao(path_cadastrada, path_escolhida);
            }
            catch (IOException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            ImageIcon imgDiferenca = new ImageIcon(path_diferenca);
            Image imageDiferenca = imgDiferenca.getImage();
            Image newimgDiferenca = imageDiferenca.getScaledInstance(lblResultadoDigital.getWidth(), lblResultadoDigital.getHeight(), java.awt.Image.SCALE_SMOOTH);
            lblResultadoDigital.setIcon(new ImageIcon(newimgDiferenca));
        }
    }//GEN-LAST:event_btnColocarDigitalActionPerformed

