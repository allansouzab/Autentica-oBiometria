package model;

/**
 *
 * @author allan
 */
public class Usuario {
    
    private String usu_id;
    private String usu_nome;
    private String usu_login;
    private String usu_senha;
    private String usu_nivel;
    private String usu_digital;

    public String getUsu_id() {
        return usu_id;
    }

    public void setUsu_id(String usu_id) {
        this.usu_id = usu_id;
    }

    public String getUsu_nome() {
        return usu_nome;
    }

    public void setUsu_nome(String usu_nome) {
        this.usu_nome = usu_nome;
    }

    public String getUsu_login() {
        return usu_login;
    }

    public void setUsu_login(String usu_login) {
        this.usu_login = usu_login;
    }

    public String getUsu_senha() {
        return usu_senha;
    }

    public void setUsu_senha(String usu_senha) {
        this.usu_senha = usu_senha;
    }

    public String getUsu_nivel() {
        return usu_nivel;
    }

    public void setUsu_nivel(String usu_nivel) {
        this.usu_nivel = usu_nivel;
    }

    public String getUsu_digital() {
        return usu_digital;
    }

    public void setUsu_digital(String usu_digital) {
        this.usu_digital = usu_digital;
    }
    
    
    
}
